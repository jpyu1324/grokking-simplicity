function multiplyByFour(x: number) {
  return x * 4;
}

function multiplyBySix(x: number) {
  return x * 6;
}

function multiplyBy12(x: number) {
  return x * 12;
}

function multiplyByPi(x: number) {
  return x * 3.14159;
}

